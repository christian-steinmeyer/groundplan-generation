public class Line {

    float x1;
    float y1;
    float x2;
    float y2;

    public Line(final float x1, final float y1, final float x2, final float y2) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }
}
